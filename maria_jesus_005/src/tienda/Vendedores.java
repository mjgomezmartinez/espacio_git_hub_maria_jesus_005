package tienda;

public class Vendedores {
	
	private String id_vendedor;
	private String nombreVendedor;
	private String apellidoVendedor;
	private String localidadVendedor;
	private String telefonoVendedor;
	private int edad;
	/**
	 * @return el id_vendedor
	 */
	private String getId_vendedor() {
		return id_vendedor;
	}
	/**
	 * @param id_vendedor el id_vendedor a establecer
	 */
	private void setId_vendedor(String id_vendedor) {
		this.id_vendedor = id_vendedor;
	}
	/**
	 * @return el nombreVendedor
	 */
	private String getNombreVendedor() {
		return nombreVendedor;
	}
	/**
	 * @param nombreVendedor el nombreVendedor a establecer
	 */
	private void setNombreVendedor(String nombreVendedor) {
		this.nombreVendedor = nombreVendedor;
	}
	/**
	 * @return el apellidoVendedor
	 */
	private String getApellidoVendedor() {
		return apellidoVendedor;
	}
	/**
	 * @param apellidoVendedor el apellidoVendedor a establecer
	 */
	private void setApellidoVendedor(String apellidoVendedor) {
		this.apellidoVendedor = apellidoVendedor;
	}
	/**
	 * @return el localidadVendedor
	 */
	private String getLocalidadVendedor() {
		return localidadVendedor;
	}
	/**
	 * @param localidadVendedor el localidadVendedor a establecer
	 */
	private void setLocalidadVendedor(String localidadVendedor) {
		this.localidadVendedor = localidadVendedor;
	}
	/**
	 * @return el telefonoVendedor
	 */
	private String getTelefonoVendedor() {
		return telefonoVendedor;
	}
	/**
	 * @param telefonoVendedor el telefonoVendedor a establecer
	 */
	private void setTelefonoVendedor(String telefonoVendedor) {
		this.telefonoVendedor = telefonoVendedor;
	}
	/**
	 * @return el edad
	 */
	private int getEdad() {
		return edad;
	}
	/**
	 * @param edad el edad a establecer
	 */
	private void setEdad(int edad) {
		this.edad = edad;
	}

}
