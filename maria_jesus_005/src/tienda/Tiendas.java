package tienda;

public class Tiendas {
	
	private String id_tienda;
	private String nombre;
	private String localidad;
	private String telefono;
	private String especialidadTienda;
	/**
	 * @return el id_tienda
	 */
	private String getId_tienda() {
		return id_tienda;
	}
	/**
	 * @param id_tienda el id_tienda a establecer
	 */
	private void setId_tienda(String id_tienda) {
		this.id_tienda = id_tienda;
	}
	/**
	 * @return el nombre
	 */
	private String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre el nombre a establecer
	 */
	private void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return el localidad
	 */
	private String getLocalidad() {
		return localidad;
	}
	/**
	 * @param localidad el localidad a establecer
	 */
	private void setLocalidad(String localidad) {
		this.localidad = localidad;
	}
	/**
	 * @return el telefono
	 */
	private String getTelefono() {
		return telefono;
	}
	/**
	 * @param telefono el telefono a establecer
	 */
	private void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	/**
	 * @return el especialidadTienda
	 */
	private String getEspecialidadTienda() {
		return especialidadTienda;
	}
	/**
	 * @param especialidadTienda el especialidadTienda a establecer
	 */
	private void setEspecialidadTienda(String especialidadTienda) {
		this.especialidadTienda = especialidadTienda;
	}

}
